package players;

public class Player {
    String side;
    int value;

    public String getSide() {
        return side;
    }

    public void setSide(String sideOfPlayer) {
        this.side = sideOfPlayer;
    }

    public int getValue() {
        return value;
    }

    public void setValue(int value) {
        this.value = value;
    }
}
